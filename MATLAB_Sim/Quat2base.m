function [ex,ey,ez] = Quat2base(x)
q1 = x(1,:);
q2 = x(2,:);
q3 = x(3,:);
q4 = x(4,:);


ex = [q1.^2-q2.^2-q3.^2+q4.^2;2*(q2.*q1+q3.*q4);2*(q3.*q1-q2.*q4)];
ey = [2*(q1.*q2 - q3.*q4);-q1.^2 + q2.^2 - q3.^2 + q4.^2;2*(q2.*q3 + q1.*q4)];
ez = [2*(q1.*q3 + q2.*q4);2*(q2.*q3 - q1.*q4);-q1.^2 - q2.^2 + q3.^2 + q4.^2];



end